//In javascript, there are two types of numbers: Regular numbers and BigInt numbers
//bigint represents a number of arbitrary length

//to make your code more readable, you can use an _ underscore to act as a separator
//it will be ignored on execution

let n = 1000000000; //one billion, 

let x = 1_000_000_000; //also one billion, but a little easier to read

console.log(x); //prints as 1000000000

console.log(""); //whitespace

//we can also use e to write it even shorter (where e represents the number of 0s)
let billion = 1e9;

console.log(billion); //prints 1000000000

   